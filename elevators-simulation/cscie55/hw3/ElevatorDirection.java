/**
 * Enumeration that distinguishes the currentDirection of each elevator in the
 * Elevators' Application.
 */
public enum ElevatorDirection
{
    /**
     * The elevator is moving upwards.
     */
    UP,

    /**
     * The elevator is moving downwards.
     */
    DOWN
}
